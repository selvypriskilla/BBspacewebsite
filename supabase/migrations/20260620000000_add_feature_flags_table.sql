-- Create audit trigger for feature flag changes
CREATE TABLE IF NOT EXISTS feature_flags_audit_log (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  flag_key TEXT NOT NULL,
  previous_state JSONB,
  new_state JSONB,
  changed_by UUID REFERENCES auth.users(id),
  changed_at TIMESTAMP DEFAULT now()
);

CREATE OR REPLACE FUNCTION audit_feature_flag_changes()
RETURNS TRIGGER AS $$
BEGIN
  INSERT INTO feature_flags_audit_log (flag_key, previous_state, new_state, changed_by)
  VALUES (
    NEW.key,
    to_jsonb(OLD),
    to_jsonb(NEW),
    auth.uid()
  );
  RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE TRIGGER feature_flag_audit
  AFTER UPDATE ON feature_flags
  FOR EACH ROW
  EXECUTE FUNCTION audit_feature_flag_changes();

-- Enable RLS on audit log
ALTER TABLE feature_flags_audit_log ENABLE ROW LEVEL SECURITY;

