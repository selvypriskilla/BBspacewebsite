-- Create feature_flags table for persistent feature flag management
-- Enables DB-backed feature rollouts, A/B testing, and gradual deployments

CREATE TABLE IF NOT EXISTS feature_flags (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  
  -- Core feature flag definition
  key TEXT UNIQUE NOT NULL,
  enabled BOOLEAN DEFAULT false,
  description TEXT,
  
  -- Rollout strategy
  rollout_percentage INTEGER DEFAULT 0 CHECK (rollout_percentage >= 0 AND rollout_percentage <= 100),
  
  -- Role-based targeting
  target_roles TEXT[] DEFAULT NULL,
  
  -- User-specific targeting
  target_user_ids UUID[] DEFAULT NULL,
  
  -- Expiration (auto-disable after date)
  expires_at TIMESTAMP NULL,
  
  -- Audit trail
  created_at TIMESTAMP DEFAULT now(),
  updated_at TIMESTAMP DEFAULT now(),
  created_by UUID REFERENCES auth.users(id) ON DELETE SET NULL,
  
  -- Indexes for queries
  CONSTRAINT feature_flags_key_unique UNIQUE (key)
);

-- Enable RLS
ALTER TABLE feature_flags ENABLE ROW LEVEL SECURITY;

-- RLS policy: Anyone can read feature flags (they're public for client-side checks)
CREATE POLICY "Enable read access for all users" ON feature_flags
  FOR SELECT USING (true);

-- RLS policy: Only admins can modify feature flags
CREATE POLICY "Enable admin write access" ON feature_flags
  FOR ALL USING (
    auth.jwt() ->> 'role' = 'authenticated' AND
    EXISTS (
      SELECT 1 FROM user_roles
      WHERE user_id = auth.uid()
      AND role = 'admin'
    )
  );

-- Create index for faster feature flag lookups
CREATE INDEX idx_feature_flags_key ON feature_flags(key);
CREATE INDEX idx_feature_flags_enabled ON feature_flags(enabled)
  WHERE enabled = true;

